export default class Operacion{
    constructor(){
    }
    sumar(arg0,arg1){
        return arg0 + arg1;
    }
    restar(arg0, arg1){
        return arg0 - arg1;
    }
    multiplicar (arg0, arg1) {
        return arg0 * arg1;
    }
    dividir(arg0, arg1) {
        return arg0 / arg1;
    }

    
}